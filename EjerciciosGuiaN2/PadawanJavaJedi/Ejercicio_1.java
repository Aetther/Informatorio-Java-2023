package EjerciciosGuiaN2.PadawanJavaJedi;

public class Ejercicio_1{
    public static void main(String[] args) {
        //Bucle While
        int i = 0;
        while (i < 3) {
            System.out.println(i);
            i++;
        }
        //Bucle Do While
        int j = 0;
        do {
            System.out.println(j);
            j++;
        } while (j < 3);
    }
}