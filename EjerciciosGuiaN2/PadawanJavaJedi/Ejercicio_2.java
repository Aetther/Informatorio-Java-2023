package EjerciciosGuiaN2.PadawanJavaJedi;

public class Ejercicio_2 {
    public static void main(String[] args) {
        //Bucle While
        int i = 0;
        while (i < 3) {
            System.out.println(i);
            i++;
        }
        //Bucle FOR
        for (int j = 0; j < 3; j++){
            System.out.println(j);
        }
    }
}
